package com.marcouno;

public class TiposDatos {
    public static void main(String[] args) {

        // Como se identifica una variable
        // TIPO IDENTIFICADOR = VALOR

        //ENTEROS
        byte number1 = 1;// 1 byte
        short number2 = 2;// 2 byte
        int number3 = 3;// 4 byte
        long number4 = 4l;// 8 byte

        //PUNTO FLOTANTE O DECIMAL
        float decimal1 = 4.9f;
        double decimal2 = 9.99d;

        //CARACTER
        char caracter1 = 'a';

        //BOOLEANOS
        boolean verdadero = true;
        boolean falso = false;

        //CADENAS DE TEXTO
        String nombre = "Alan";
        String apellido ="Sastre";

        //TIPOS ENVOLTORIO
        Integer numero = null;
        Long numero2 = 2L;



    }
}
